    public void drawLandmarksOnMap(GoogleMap map) {
        for (Landmark lm : landmarks) {
            map.addCircle(new CircleOptions()
                    .center(new LatLng(lm.latitude, lm.longitude))
                    .radius(30)
                    .strokeColor(0xFF0000FF)
                    .fillColor(0x220000FF)
                    .strokeWidth(2));
        }
    }

    public List<Landmark> getNearbyLandmarks(Location userLocation, float radiusMeters) {
        List<Landmark> nearby = new ArrayList<>();
        for (Landmark lm : landmarks) {
            float[] results = new float[1];
            Location.distanceBetween(
                    userLocation.getLatitude(), userLocation.getLongitude(),
                    lm.latitude, lm.longitude, results
            );
            if (results[0] <= radiusMeters) {
                nearby.add(lm);
            }
        }
        return nearby;
    }
